const canvas = document.getElementById('antCanvas');
const ctx = canvas.getContext('2d');

const playBtn = document.getElementById('playBtn');
const pauseBtn = document.getElementById('pauseBtn');
const resetBtn = document.getElementById('resetBtn');
const speedSlider = document.getElementById('speedSlider');
const speedValue = document.getElementById('speedValue');
const stepCounter = document.getElementById('stepCounter');

let simulationData = [];
let currentStep = 0;
let isPlaying = false;
let animationId;
let stepsPerFrame = parseInt(speedSlider.value);

// Canvas/Grid configs
const GRID_WIDTH = 200;
const GRID_HEIGHT = 200;
const CELL_SIZE = canvas.width / GRID_WIDTH; // 800/200 = 4 pixels per grid cell

// Fetch CSV Data generated by our C++ program
async function loadData() {
    try {
        const response = await fetch('data.csv');
        const text = await response.text();
        parseCSV(text);
        drawInitialState();
    } catch (error) {
        console.error("Error loading CSV:", error);
        alert("Failed to load data.csv. Make sure the C++ code ran successfully!");
    }
}

function parseCSV(csvText) {
    const lines = csvText.trim().split('\n');
    // Skip header line
    for (let i = 1; i < lines.length; i++) {
        if (!lines[i]) continue;
        const parts = lines[i].split(',');
        simulationData.push({
            step: parseInt(parts[0]),
            x: parseInt(parts[1]),
            y: parseInt(parts[2]),
            color: parseInt(parts[3]), // 1 for black, 0 for white
            dx: parseInt(parts[4]),
            dy: parseInt(parts[5])
        });
    }
    stepCounter.innerText = `0 / ${simulationData.length}`;
}

function getThemeColor(varName) {
    return getComputedStyle(document.body).getPropertyValue(varName).trim();
}

function drawInitialState() {
    ctx.fillStyle = getThemeColor('--canvas-bg');
    ctx.fillRect(0, 0, canvas.width, canvas.height);
}

function drawStep(dataLine) {
    ctx.fillStyle = dataLine.color === 1 ? getThemeColor('--text-primary') : getThemeColor('--canvas-bg');
    ctx.fillRect(dataLine.x * CELL_SIZE, dataLine.y * CELL_SIZE, CELL_SIZE, CELL_SIZE);
    
    // We intentionally do not clear the ant's previous position explicitly 
    // to visualize the emergent trail ("highway") perfectly.
}

function renderFrame() {
    if (!isPlaying || currentStep >= simulationData.length) {
        isPlaying = false;
        return;
    }

    // Process multiple steps per frame to speed up simulation
    for (let i = 0; i < stepsPerFrame; i++) {
        if (currentStep >= simulationData.length) {
            isPlaying = false;
            break;
        }
        drawStep(simulationData[currentStep]);
        currentStep++;
    }

    stepCounter.innerText = `${currentStep} / ${simulationData.length}`;

    if (isPlaying) {
        animationId = requestAnimationFrame(renderFrame);
    }
}

// Event Listeners
playBtn.addEventListener('click', () => {
    if (!isPlaying && currentStep < simulationData.length) {
        isPlaying = true;
        renderFrame();
    }
});

pauseBtn.addEventListener('click', () => {
    isPlaying = false;
    cancelAnimationFrame(animationId);
});

resetBtn.addEventListener('click', () => {
    isPlaying = false;
    cancelAnimationFrame(animationId);
    currentStep = 0;
    drawInitialState();
    stepCounter.innerText = `0 / ${simulationData.length}`;
});

speedSlider.addEventListener('input', (e) => {
    stepsPerFrame = parseInt(e.target.value);
    speedValue.innerText = stepsPerFrame;
});

// Theme Toggle Logic
const themeToggleBtn = document.getElementById('themeToggle');

function applyTheme(theme) {
    document.body.classList.remove('light-theme', 'dark-theme');
    document.body.classList.add(theme);
    localStorage.setItem('theme', theme);
}

const savedTheme = localStorage.getItem('theme');
if (savedTheme) {
    applyTheme(savedTheme);
}

themeToggleBtn.addEventListener('click', () => {
    let currentTheme = 'light-theme';
    if (document.body.classList.contains('dark-theme')) {
        currentTheme = 'dark-theme';
    } else if (!document.body.classList.contains('light-theme') && window.matchMedia('(prefers-color-scheme: dark)').matches) {
        currentTheme = 'dark-theme';
    }
    
    const newTheme = currentTheme === 'dark-theme' ? 'light-theme' : 'dark-theme';
    applyTheme(newTheme);
    
    // Redraw canvas with new theme colors
    drawInitialState();
    const stepsToDraw = currentStep;
    currentStep = 0;
    const oldStepsPerFrame = stepsPerFrame;
    stepsPerFrame = stepsToDraw;
    if (stepsToDraw > 0) renderFrame();
    stepsPerFrame = oldStepsPerFrame;
});

// Start process
loadData();
